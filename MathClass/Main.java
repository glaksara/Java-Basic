public class Main {
    public static void main(String[] args) {
//        Pi
        System.out.println(Math.PI);

//        Power
        double result;
        result = Math.pow(2, 3);
        System.out.println(result);

//        Absolute
        result = Math.abs(-5);
        System.out.println(result);

//        Square root
        result = Math.sqrt(9);
        System.out.println(result);

//        Round
        result = Math.round(3.55);
//        Round Up
        result = Math.ceil(3.14);
//        Round Down
        result = Math.floor(3.14);
        System.out.println(result);
    }
}
